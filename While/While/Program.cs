using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace While
{
    class Program
    {
        static void Main(string[] args)
        {
            int cont = 1;

            while (cont <= 10)
            {
                Console.Write(cont + " ");
                cont++;
            }

        }
    }
